    /**
     * Display the specified resource.
     *
     * @param  \App\Post  $post
     * @return \Illuminate\Http\Response
     */
    public function show($slug, $id)
    {
        $series = Serie::published()->where('slug', $slug)->firstOrFail();
        $post = $series->posts()->published()->findOrFail($id);
        if($post->auth_locked && !Auth::check()) {
            abort(404);
        }
        return view('posts.show', ['post' => $post, 'series' => $series]);
    }

